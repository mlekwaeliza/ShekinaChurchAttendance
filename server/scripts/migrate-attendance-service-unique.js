const { Client } = require('pg');
const path = require('path');

require('dotenv').config({ path: path.resolve(__dirname, '..', '.env') });

const migrationName = '2026-06-04-attendance-service-unique';

async function main() {
  if (!process.env.DATABASE_URL) {
    console.log('No DATABASE_URL - skipping');
    return;
  }

  const client = new Client({
    connectionString: process.env.DATABASE_URL,
    ssl: { rejectUnauthorized: false }
  });

  await client.connect();
  console.log('Connected.');

  try {
    await client.query('BEGIN');

    await client.query(`
      CREATE TABLE IF NOT EXISTS schema_migrations (
        id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        name TEXT UNIQUE NOT NULL,
        applied_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
      )
    `);

    const existing = await client.query(
      'SELECT id FROM schema_migrations WHERE name = $1',
      [migrationName]
    );
    if (existing.rowCount > 0) {
      await client.query('COMMIT');
      console.log('Migration already applied.');
      return;
    }

    await client.query('ALTER TABLE attendance ADD COLUMN IF NOT EXISTS service_type_id INTEGER');
    await client.query(`
      UPDATE attendance
      SET service_type_id = 1
      WHERE service_type_id IS NULL
    `);
    await client.query(`
      UPDATE attendance a
      SET service_type_id = 1
      WHERE NOT EXISTS (
        SELECT 1 FROM service_types st WHERE st.id = a.service_type_id
      )
    `);

    await client.query('ALTER TABLE attendance DROP CONSTRAINT IF EXISTS attendance_member_id_date_key');
    await client.query('ALTER TABLE attendance DROP CONSTRAINT IF EXISTS attendance_member_id_date_service_type_id_key');
    await client.query('ALTER TABLE attendance DROP CONSTRAINT IF EXISTS attendance_service_type_id_fkey');

    await client.query('ALTER TABLE attendance ALTER COLUMN service_type_id SET DEFAULT 1');
    await client.query('ALTER TABLE attendance ALTER COLUMN service_type_id SET NOT NULL');
    await client.query(`
      ALTER TABLE attendance
      ADD CONSTRAINT attendance_service_type_id_fkey
      FOREIGN KEY (service_type_id) REFERENCES service_types(id) ON DELETE RESTRICT
    `);
    await client.query(`
      ALTER TABLE attendance
      ADD CONSTRAINT attendance_member_id_date_service_type_id_key
      UNIQUE (member_id, date, service_type_id)
    `);

    await client.query(
      'INSERT INTO schema_migrations (name) VALUES ($1)',
      [migrationName]
    );

    await client.query('COMMIT');
    console.log('Migration applied:', migrationName);
  } catch (error) {
    await client.query('ROLLBACK');
    throw error;
  } finally {
    await client.end();
  }
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
